package main.java.ar.edu.untref.industrial.model;

public class Satelite {

	private int prn;
	private int elev;
	private int az;
	private int snr;
	
	public int getPrn() {
		return prn;
	}
	public void setPrn(int prn) {
		this.prn = prn;
	}
	public int getElev() {
		return elev;
	}
	public void setElev(int elev) {
		this.elev = elev;
	}
	public int getAz() {
		return az;
	}
	public void setAz(int az) {
		this.az = az;
	}
	public int getSnr() {
		return snr;
	}
	public void setSnr(int snr) {
		this.snr = snr;
	}
	
}
